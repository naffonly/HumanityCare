            <h1 style="font-weight: bold;">SIAGA GUNUNG MELETUS</h1>
            <h4>Apa Itu Letusan Gunung Api ?</h4>
            <p>Gunung api meletus disebabkan maga di dalam perut bumi yang di dorong keluar oleh gas yang bertekanan tinggi atau karena pergerakan 
                lempengan bumi,tumpukan tekanan dan panas cairan magma. Letusannya membawa abu dan batu yang menyenbur dengan keras, sedangkan lavanya
                 bisa membanjiri daerah sekitarnya bahkan hingga radius ribuan kilometer.</p>

              <h4>Pusat Vulkanologi dan Mitigasi Bencana Geologi membagi status gunung api aktif menjadi : </h4>
              <ul>
                <li>AWAS : Letusan dapat menjadi dalam waktu 24 jam.</li>
                <li>SIAGA : Letusan dapat menjadi dalam waktu 2 minggu</li>
                <li>WASPADA : Aktifitas vulkanik san seismik/kegempaan gunung sudah meningkat</li>
                <li>NORMAL : Tidak ada gejala tekanan magma.</li>
              </ul>  
              <center><img src="../asset/img/05122017-siaga-bencana-letusan-gunung-api-1.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
              <h4>Bahaya letusan gunung berapi adalah adanya erupsi, yang dapat berupa : </h4>
              <ul>
                <li>Awan panas , Bersuhu 300 sampai 700 derajat celcius, berkecepatan lebih dari 70 km/jam.</li>
                <li>Material vulkanik, berupa batuan panas bersuhu lebih dari hujan abu yang mengandung asam dan berbahaya bagi pernafasan ,penglihatan,merusak air tanah dan tanaman</li>
               <li>Lava atau lahar ,yang berwujud cairan kental bersuhu antara 700 sampai 1200 derajat celcius.</li>
               <li>Gas beracun yang dapat keluar melalu rongga atau kawah bersama letusan , yang dapat mematikan makhluk hidup.</li>
               <li>Tsunami, bilamana gunung api yang berada di bawah permukaan laut meletus.</li>
              </ul>

                <h4> Yang Harus Dilakukan Sebelum Terjadi Letusan</h4>
                <ul>
                    <li>Kenali kawasan rawan bencana letusan gunung api</li>
                    <li>Sepakati tempat berkumpul bersama warga sekitar.</li>
                    <li>Menentukan jalur penyelamatan /evakuasi yang terdekat dari rumah untuk menuju tempat berkumpul</li>
                    <li>Siapkan tas khusus yang berisi kebutuhan hidup selama tiga hari seperti,makanan,pakaian dan obat-obatan.</li>
                    <li>Mengikutin latihan penyelamatan /evakuasi secara berkala untuk menyelamatkan diri menuju lokasi yang aman pada saat terjadi gempa.</li>
                </ul>
                <h4> Yang Harus Dilakukan Saat Terjadi Letusan</h4>
                    <ul>
                    <li>Segera mengungsi apabila sudah ada perintah mengungsi dari instansi/badan yang berwenang.</li>
                    <li>Hindari melewati jalur yang searah dengan arah angin dan sungai yang berhulu di puncak gunung yang sedang meletus.</li>
                    <li>Hindari daerah bahaya yang telah di tetapkan dengan melihat peta ancaman bahaya letusan gunung api.</li>
                    <li>Apabila melihat permukaan aliran air sungai naik,cepat-cepat cari daerah yang lebih tinggi.</li>
                    <li>Apabila terjebak dalam ruangan ,tutup semua pintu dan jendela.</li>
                    <li>Apabila berada di ruang terbuka cari ruang perlindungan darurat.</li>
                    <li>Apabila terjadi hujan batu,lindungi kepala dengan posisi menunduk dengan tubuh condong kedepan untuk melindungi dada serta silangkan tangan di antara kepalan dan tengkuk untuk melindungi kepala .</li>
                    <li>Saat turun hujan abu,jangan memakai lensa kontak(bagi yang menggunkanannya),usahakan menutup wajah dengan kedua telapak tangan atau sapu tangan serta gunakan kain / masker untuk melindungi pernafasan</li>
                </ul>
                <h4> Yang Harus Dilakukan Setelah Terjadi Letusan</h4>
                <ul>
                    <li>Kembali kerumah setelah situasi di nyatakan aman oleh pihak berwenang</li>
                    <li>Bersihkan atap rumah dari debu/abu gunung hujan debu yang menutupi atap rumah apabila tebal akan sangat beratdan dapat mengakibatkan runtuhnya atap rumah.</li>
                </ul>
                <h4>Pasca Letusan</h4>
                <ul>
                    <li>Apabila memungkiknan hindari daerah-daerah zona hujan abu</li>
                
                    <ol type="a">
                        <li>tutup mulut dan hidung anda.debu gunung api dapat mengiritasi sistem pernafasan anda.</li>
                        <li>gunakan kacamata untuk melindungi mata anda</li>
                        <li>lindungi kulit anda dari iritasi akibat debu gunung api</li>
                        <li>bersihkan atap akibat dari hujan debu gunung berapi</li>
                        <li>hujan debu yang menutupi atap sangat berat dan dapat mengakibatkan runtuhnya atap bangunan. Hati-hati ketika bekerja di atap bangunan rumah</li>
                        </ol>  
                    <li> Hindari mengendarai kendaraan di daerah hujan yang lebat</li>
                    <li>Mengendarai kendaraan mengakibatkan debu tersedot dan dapat merusak mesin kendaraan tersebut</li>
                    <li>Apabila anda mempunyai penyakit pernafasan ,hindari sedapat mungkin kontak dengan debu gunung api</li>
                    <li>Tinggallah di dalam ruangan,samapai keadaan dinyatakan aman di luar rumah</li>
                    <li>Ingat untuk membantu tatngga yang mungkin membutuhkan pertolongan seperti orang tua,orang yang cacat fisik,dsb.</li>
                </ul>
