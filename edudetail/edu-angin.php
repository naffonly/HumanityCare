            <h1 style="font-weight: bold;">SIAGA BENCANA PUTING BELIUNG</h1>
            <p>Angin Puting beliung adalah angin yang berputar dengan kecepatan lebih dari 63 km/jam yang bergerak secara garis 
                lurus dengan lama kejadian maksimum 5 menit.Angin jenis lain dengan ukuran lebih besar yang ada di Amerika yaitu 
                Tornado mempunyai kecepatan sampai 320 km/jam dan berdiameter 500 meter. Angin puting beliung sering terjadi pada 
                siang hari atau sore hari pada musim pancaroba. Angin ini dapat menghancurkan apa saja yang diterjangnya, karena 
                dengan pusarannya benda yang terlewati terangkat dan terlempar.</p>

              <h4>Gejala Awal Puting Beliung</h4>
              <ul>
                <li>Udara terasa panas dan gerah (sumuk).</li>
                <li>Di langit tampak ada pertumbuhan awan Cumulus (awan putih bergerombol yang berlapis-lapis).</li>
                <li>Diantara awan tersebut ada satu jenis awan mempunyai batas tepinya sangat jelas bewarna abu-abu menjulang tinggi yang secara visual seperti bunga kol.</li>
                <li>Awan tiba-tiba berubah warna dari berwarna putih menjadi berwarna hitam pekat (awan Cumulonimbus).</li>
                <li>Ranting pohon dan daun bergoyang cepat karena tertiup angin disertai angin kencang sudah menjelang.</li>
                <li>Durasi fase pembentukan awan, hingga fase awan punah berlangsung paling lama sekitar 1 jam. Karena itulah, masyarakat agar tetap waspada selama periode ini.</li>
              </ul>  
              <h4>Karateristik Puting Beliung</h4>
              <ul>
                  <li>Puting berliung merupakan dampak ikutan awan Cumulonimbus (Cb) yang biasa tumbuh selama periode musim hujan, tetapi tidak semua pertumbuhan awan CB akan menimbulkan angin puting beliung.</li>
                  <li>Kehadirannya belum dapat diprediksi.</li>
                  <li>Terjadi secara tiba-tiba (5-10 menit) pada area skala sangat lokal.</li>
                  <li>Pusaran puting beliung mirip belalai gajah/selang vacuum cleaner.</li>
                  <li>Jika kejadiannya berlangsung lama, lintasannya membentuk jalur kerusakan.</li>
                  <li>Lebih sering terjadi pada siang  hari dan lebih banyak di daerah dataran rendah.</li>
              </ul>
              <center><img src="../asset/img/05122017-siaga-bencana-puting-beliung.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
                <h4> Yang Harus Diperhatikan Sebelum Datangnya Angin</h4>
                <ul>
                    <li>Dengar dan simaklah siaran radio atau televisi menyangkut prakiraan terkini cuaca setempat</li>
                    <li>Waspadalah terhadap perubahan cuaca</li>
                    <li>Waspadalah terhadap angin topan yang mendekat</li>
                    <li>Waspadalah terhadap tanda tanda bahaya sebagai berikut</li>
                    <li>Langit gelap, sering berwarna kehijauan</li>
                    <li>Hujan es dengan butiran besar</li>
                    <li>Awan rendah, hitam, besar, seringkali bergerak berputar</li>
                    <li>Suara keras seperti bunyi kereta api cepat</li>
                    <li>Bersiaplah untuk ke tempat perlindungan (bunker) bila ada angin topan mendekat</li>
                </ul>
                <h4>Beberapa Tindakan Yang dilakukan untuk Menghindari Korban Saat Puting Beliung</h4>
                <ul>
                    <li>Masukkan barang-barang Anda ke dalam rumah, agar tidak terbawa angin.</li>
                    <li>Tutup jendela dan pintu lalu kunci rapat-rapat. Bila dibutuhkan ikatlah pintu/jendela dengan tali atau papan tambahan agar tidak terdampak oleh angin dapat dilakukan</li>
                    <li>Matikan semua aliran listrik dan peralatan elektronik agar tidak tersambar oleh petir yang biasa terjadi saat angin puting  beliung.</li>
                    <li>Hindari berada di daerah terbuka dan di bawah pohon atau papan reklame. Jangan parker kendaraan Anda di bawah pohon, papan reklame atau bangunan yang potensial rubuh</li>
                    <li>Segera masuk ke dalam rumah atau bangunan yang kokoh.</li>
                </ul>
                <h4>Yang Harus Diperhatikan Saat Puting Beliung</h4>
                <ul>
                    <li>Berlindung pada bangunan yang kokoh dan aman begitu angin kencang menerjang</li>
                    <li>Segera menjauh dari lokasi kejadian jika memungkinkan, karena puting beliung berlangsung sangat cepat</li>
                    <li>Jika sedang di dalam rumah semi permanen / rumah kayu hingga bangunan bergoyang, segeralah keluar rumah untuk mencari perlindungan di tempat lain karena bisa jadi rumah tersebut akan roboh</li>
                    <li>Hindari berteduh dibawah pohon besar, baliho, papan reklame dan jalur kabel listrik</li>
                    <li>Bersabarlah untuk tetap berlindung di tempat aman. Angin putting beliung biasanya terjadi 5-10 menit</li>
                </ul>
                <h4>Yang Harus Dilakukan Setelah Bencana </h4>
                <ul>
                    <li>Melakukan koordinasi dengan berbagai pihak dalam pertolongan para korban</li>
                    <li>Mendirikan posko evakuasi untuk korban selamat</li>
                    <li>Mendirikan tempat penampungan korban bencana saat darurat</li>
                    <li>Melakukan koordinasi bahan bantuan</li>
                    <li>Melakukan evaluasi pelaksanaan pertolongan dan perkiraan kerugian material</li>
                </ul>
