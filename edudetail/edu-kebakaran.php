            <h1 style="font-weight: bold;">SIAGA KEBAKARAN HUTAN</h1>
            <p>Kebakaran hutan atau lahan adalah perubahan langsung atau tidak langsung terhadap keadaan fisik hutan atau lahan akibat dari
                penggunaan api yang dapat mengakibatkan terjadinya kebakaran hutan atau lahan. Akibat kebakaran, hutan atau lahan menjadi tidak berfungsi 
                dan dipakai lagi dalam menunjang kehidupan berkelanjutan.</p>

              <h4>Penyebab</h4>
              <ul>
                <li>Aktivitas manusia menggunaka api di kawasan hutan dan lahan</li>
                <li>Faktor alam yang dapat memicu terjadinya kebakaran hutan dan lahan</li>
                <li>Angin yang cukup besar dapat memicu dan mempercepat menjalarnya api</li>
              </ul>  
              <center><img src="../asset/img/siaga-bencana-kebakaran.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
              <h4>Tindakan Yang Dilakukan Sebelum Terjadinya Bencana</h4>
              <ul>
                <li>Pastikan semua peralatan pencegahan kebakaran hutan dan lahan telah tersedia dan lengkap.</li>
               <li>Jangan tinggalkan anak-anak bermain sendiri dirumah tanpa pengawasan</li>
               <li>Larang anak-anak bermain ke arah utan atau lahan yang mungkin terbakar </li>
               <li>Pangkas semua cabang pohon yang mati mengenai rumah</li>
               <li>Lpor kepada petugas kehutanan bila terlihat ada hutan dan lahan terbakar</li>
               <li>Persiapkan masker dan kacamata untuk melindungi dari asap</li>
              </ul>
              <h4>Tindakan Yang Perlu Dilakukan Saat Terjadi Kebakaran Hutan</h4>
              <ul>
                  <li>Melakukan identifikasi masalah dan analisa mengenai penyebab kebakaran hutan</li>
                  <li>Mengumpulkan data-data penting mengenai keterangan dari kebakaran hutan atau lahan secara rinci dan jelas</li>
                  <li>Pengawasan dan evaluasi kejadian untuk mencari sumber titik api pertama kebakaran hutan</li>
                  <li>Segera mencari sumber air terdekat dari lokasi kebakaran hutan atau lahan</li>
                  <li>Meminta bantuan warga atau masyarakat sekitar untuk ikut serta memadamkan api bersama-sama agar api lebih cepat padam dan tidak menyebar terlalu jauh</li>
                  <li>Meminta bantuan petugas hutan dan pemerintah setempat jika kebakaran hutan atau lahan terjadi dalam skala besar dan perlu peralatan yang lebih canggih, misalnya mobil pemadam kebakaran dan lainnya. Hal ini juga berkaitan dengan bantuan penanggulangan bencana alam.</li>
            </ul>
              <h4>Tindakan Untuk Mengurangi Risiko Kerugian Bencana Kebakaran </h4>
                  <ul>
                   <li>Membuat dan melakukan sosialisasi tentang kebijakan pencegahan dan penanganan kebakaran.</li>
                   <li>Meningkatkan penegakan hukum.</li>
                   <li>Membentuk pasukan pemadaman kebakaran, khususnya untuk penanganan kebakaran secara dini.</li>
                   <li>Membuat waduk-waduk kecil, seperti penampungan air dan hydrant untuk memadamkan api.</li>
                   <li>Melakukan pengawasan atas pembakaran lahan secara ketat.</li>
                   <li>Meningkatkan partisipasi aktif dalam pemadaman awal kebakaran.
                </ul>

              
