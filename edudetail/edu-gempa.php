            <h1 style="font-weight: bold;">SIAGA GEMPA BUMI</h1>
            <p>Gempa Bumi adalah getaran di tanah yang disebabkan oleh pergerakan permukaan bumi. 
                Episentrum adalah titik di permukaan bumi, tepat ditas pusat gempa. 
                Hiposentrum berada jauh dalam tanah ditempat batuan pecah 7 bergeser untuk pertama kali.
                Gempa Bumi yang kuat dapat menyebabkan kerusakan besar bagi gedung, jembatan dan bangunan lain, termasuk juga korban nyawa. </p>

              <h4>Jenis - Jenis Gempa Bumi</h4>
              <ul>
                <li>Gempa Tektonik  : Adalah getaran kuat yang diakibatkan oleh patahan bumi karena pergesekan lempeng samudera atau lempeng bumi.</li>
                <li>Gempa Vulkanik : Adalah getaran kuat akibat kegiatan gunung berapi.</li>
              </ul>  
              <center><img src="../asset/img/05122017-siaga-bencana-gempa-bumi-1.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
              <h4>Tanda - Tanda Terjadinya Gempa </h4>
              <ul>
                <li>Di Dalam Bangunan</li>
                <p>Semua benda yang tergantung bergoyang dan berjatuhan, misalnya : lampu gantung, pigura, jam dinding, lukisan dan lai-lain. Semua benda yang berdiri atau terletak diatas meja bergeser dan berjatuhan, misalnya : TV, radio, jam, alat makan, kompor dll.</p>
                <li>Di Luar Bangunan</li>
                <p> Pohon, tiang listrik dan lampu jalan, jembatan serta gedung bergetar, bahkan jika terjadi getaran sangat kuat akan mengakibatkan tumbang dan roboh. Retakan/rekahan akan terlihat jelas pada permukaan tanah, dinding bangunan, dan jembatan.</p>
              </ul>

                <h4> Yang Harus Dilakukan Saat Terjadi Gempa</h4>
                <p>Di Dalam Bangunan</p>
                <ul>
                    <li>Bila memungkinkan, segera cari jalan keluar yang aman</li>
                    <li>Bersembunyi dibawah meja, untuk menghindari reruntuhan</li>
                    <li>Hindari berada di dekat lemari, lemari es dan benda-benda yang mungkin bisa rubuh</li>
                    <li>Jangan berlari keluar dengan tergesa-gesa/panik</li>
                </ul>
                    <p>Di Luar Bangunan</p>
                    <ul>
                    <li>Hindari bangunan tinggi, jembatan, tiang listrik, papan reklame</li>
                    <li>Jangan mendekati pohon-pohon yang tinggi</li>
                    <li>Cari tempat terbuka, atau tanah lapang</li>
                </ul>

                <h4> Yang Harus Dilakukan Setelah Terjadi Gempa</h4>
                <ul>
                    <li>Jangan segera masuk ke bangunan setelah terjadi gempa, karena kemungkinan akan terjadi gempa susulan</li>
                    <li>Segera berikan pertolongan pertama terhadap korban gempa</li>
                    <li>Cari informasi lebih lanjut dari pihak yang berwenang tentang gempa yang terjadi</li>
                </ul>

