            <h1 style="font-weight: bold;">SIAGA TSUNAMI</h1>
            <p>Indonesia termasuk salah satu kawasan yang rawan bencana tsunami.
            Tsunami sendiri memiliki kecepatan gelombang yang setara dengan pesawat jet sehingga mampu menghancurkan berbagai benda
             yang berada di dalam jangkauan gelombangnya.</p>
             <p>Agar aman dari terjangan tsunami, ada beberapa persiapan yang wajib Anda lakukan seperti:</p>

              <h4>Sebelum Terjadi Tsunami</h4>
              <ul>
                <li>Rutin mengikuti pelatihan evakuasi tsunami yang nantinya bermanfaat untuk menyelamatkan diri pada saat terjadi tsunami.</li>
                <li>Menentukan jalur evakuasi yang dekat dengan rumah dan pastikan tempat tersebut berada di kawasan yang lebih tinggi.</li>
                <li>Membawa beberapa barang di dalam tas khusus yang cukup untuk hidup selama tiga hari seperti obat-obatan, makanan, pakaian, dan minuman.</li>
              </ul>  
              <center><img src="../asset/img/05122017-siaga-bencana-tsunami-1-2.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
              <h4> Saat Terjadi Tsunami</h4>
              <ul>
                <li>Pastikan untuk memerhatikan peringatan sekaligus arahan dari petugas setempat saat proses evakuasi.</li>
                <li>Di kawasan yang rawan tsunami biasanya terdapat jalur-jalur untuk evakuasi, ikuti jalur tersebut dan pastikan untuk menghindari jembatan.</li>
                <li>Jika sedang berada di dalam rumah, berusahalah untuk tetap tenang dan segera bimbing keluarga untuk menyelamatkan diri menuju tempat yang aman dan tinggi. Atau apabila berada di dekat pantai, segeralah menuju tempat yang lebih tinggi dan menjauhlah minimal 3 km dari pantai dan 6 km dari atas permukaan laut.</li>
                <li>Pada saat menuju tempat aman, sangat dianjurkan untuk berjalan kaki. Akan tetapi, jika menggunakan kendaraan dan terkena macet, segera kunci dan tinggalkan kendaraan tersebut kemudian lanjutkan evakuasi dengan berjalan kaki.</li>
                <li>Setelah sampai di kawasan yang lebih tinggi, bertahanlah di sana dan jangan sampai turun sebelum keadaan dinyatakan aman oleh pihak berwenang. Dan jangan sampai lupa selalu dengarkan informasi melalui radio ataupun alat komunikasi. </li>
               
              </ul>

                <h4> Setelah Terjadi Tsunami</h4>
                <ul>
                    <li>Setelah keadaan dinyatakan aman, barulah kembali ke rumah masing-masing.</li>
                    <li>Jika rumah Anda dalam keadaan baik dan tidak terkena terjangan tsunami, segera bersihkan untuk menghindari kemungkinan penyakit bersarang di sana.</li>
                    <li>Setelah selesai membersihkan rumah, selanjutnya ikutlah berpartisipasi membersihkan lingkungan sekitar.</li>
                    
                </ul>

              
