            <h1 style="font-weight: bold;">SIAGA BANJIR</h1>
            <p>Bencana banjir hampir setiap musim penghujan melanda Indonesia. 
              Berdasarkan nilai kerugian dan frekuensi kejadian bencana banjir terlihat adanya peningkatan yang cukup berarti. 
              Kejadian bencana banjir tersebut sangat dipengaruhi oleh faktor alam berupa curah hujan diatas normal dan adanya pasang naik air laut.
              Disamping itu faktor ulah manusia juga berperan penting seperti penggunaan lahan yang tidak tepat (pemukiman di daerah bantaran sungai 
              dan daerah resapan air) penggundulan hutan, pembuangan sampah, kedalam sunga in dsb.</p>
             
              <h4>Kenali Penyebab Banjir</h4>
              <ul>
                <li>Curah hujan tinggi</li>
                <li>Permukaan tanah lebih rendah dibandingkan muka air laut.</li>
                <li>Terletak pada suatu cekungan yang dikelilingi perbukitan dengan pengaliran airkeluar sempit</li>
                <li>Banyak pemukiman yang dibangun pada dataran sepanjang sungai.</li>
                <li>Aliran sungai tidak lancar akibat banyaknya sampah serta bagunan din pinggir sungai</li>
                <li>Kurangnya tutupan lahan di daerah hulu sungai</li>
              </ul>  
              <center><img src="../asset/img/05122017-siaga-bencana-banjir-1.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>
              <h4> Mengurangi Dampak Banjir</h4>
              <ul>
                <li>Penataan daerah aliran sungai secara terpadu dan sesuai fungsi lahan.</li>
                <li>Pembangunan sistem pemantauan dan peringatan dini pada bagian sungai yang sering menimbulkan banjir.</li>
                <li>Tidak membangun rumah dan pemukiman di bantaran sungai serta daerah banjir</li>
                <li>Tidak membuang sampah kedalam sungai. Mengadakan program pengerukan sungai.</li>
                <li>Pemasangan pompa untuk daerah yang lebih rendah dari permukaan laut.</li>
                <li>Program penghijauan daerah hulu sungai harus selalu dilaksanakan serta mengurangi aktifitas dibagian rawan banjir.</li>
              </ul>

                <h4> Yang Harus Dilakukan Saat Banjir</h4>
                <ul>
                    <li>Matikan aliran listrik di dalam rumah atau hubungi PLN untuk mematikan aliran listrik di wilayah yang terkena bencana</li>
                    <li>Mengungsi ke daerah aman sedini mungkin saat genangan air masih memungkinkan untuk disebrangi</li>
                    <li>Hindari berjalan di dekat saluran air untuk menghindari terseret arus banjir.</li>
                    <li>Segera mengamankan barang-barang berharga ketempat yang lebih tinggi.</li>
                    <li>Jika air terus meninggi hubungi instansi yang terkait dengan penanggulangan bencana seperti Kantor Kepala Desa, Lurah ataupun Camat</li>
                </ul>

                <h4> Yang Harus Dilakukan Setelah Banjir</h4>
                <ul>
                    <li>Secepatnya membersihkan rumah, dimana lantai pada umumnya tertutup lumpur dan gunakan antiseptik untuk membunuh kuman penyakit.</li>
                    <li>Cari dan siapkan air bersih untuk menghindari terjangkitnya penyakit diare yang sering berjangkit setelah kejadian banjir.</li>
                </ul>

                <h4> Cegah Banjir Dengan Cara</h4>
                <ul>
                    <li>Menjaga kebersihan lingkungan</li>
                    <li>Tanam pohon di sepanjang aliran sungai</li>
                    <li>Bersihkan saluran air secara berkala</li>
                </ul>
              
