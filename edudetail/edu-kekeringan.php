            <h1 style="font-weight: bold;">SIAGA BENCANA KEKERINGAN</h1>
            <p>Kekeringan  merupakan kondisi kekurangan pasokan air dan curah hujan dalam jangka waktu tertent, biasanya satu 
                musim atau lebih, yang berakibat pada kekurangan air untuk beberapa sektor kegaitan, kelompok atau lingkungan (UNISDR, 2019)</p>

              <h4>Prabencana</h4>
              <ul>
                <li>Menjaga sumber mata air</li>
                <li>Menggunakan air dengan bijak</li>
                <li>Tidak merusak hutan/kawasan cagar alam</li>
                <li>Secara kolektif membuat waduk atau embung untuk menampung air hujan dan dipergunakan saat musim kemarau</li>
                <li>Dalam konteks pertanian, memanfaatkan mulsa. Mulsa adalah meterial penutup tanaman budidaya untuk menjaga kelembaban tanah serta menekan pertumbuhan gulma dan penyakit sehingga tanaman dapat tumbuh dengan baik.</li>
                <li>Memenuhi kebutuhan keluarga, membuat tandon air di sekitar pekarangan rumah untuk menampung air hujan</li>
              </ul>  
              <center><img src="../asset/img/05122017-siaga-bencana-kekeringan-1.jpg" class="img-fluid my-2" alt="..." width = "75%" style="border-radius: 30px;"></center>

                <h4> Yang Harus Dilakukan Saat Terjadi Kekeringan</h4>
                <ul>
                    <li>Melapor dan meminta bantuan air bersih pada pihak yang berwenang</li>
                    <li>Mengatur jadwal penggunaan air yang masih ada</li>
                    <li>Pelaksanaan hujan buatan/TMC</li>
                    <li>Simak informasi terkini dari radio, televisi, media online dan informasi resmi dari pemerintah</li>
                </ul>

                <h4>Pascabencana</h4>
                <ul>
                    <li>Membuat sumur resapan/biopori</li>
                    <li>Membuat waduk/bendungan untuk menampung air hujan</li>
                    <li>Cari informasi lebih lanjut dari pihak yang berwenang tentang gempa yang terjadi</li>
                </ul>
                <h4>Langkah-Langkah Mitigasi Bencana Kekekringan</h4>
                <ul>
                    <li>Melakukan pengelolaan air secara bijaksana, yakni dengan mengganti penggunaan air tanah dengan air permukaan dengan cara membuat waduk dan saluran distribusi yang efisien.</li>
                    <li>Konservasi tanah dan mengurangi tingkat erosi dengan pembuatan check dam serta reboisasi.</li>
                    <li>Mengganti kayu bakar menjadi bahan bakar minyak untuk menghindari penebangan hutan.</li>
                    <li>Melakukan pendidikan dan pelatihan.</li>
                    <li>Memperbaiki daerah yang tandus dengan melakukan pengelolaan lahan, hutan, waduk resapan, dan irigasi.</li>

